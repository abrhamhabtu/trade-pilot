// ─── Key Levels Visualizer ────────────────────────────────────────────────────

function KeyLevelsPanel({ card }: { card: SetupCard }) {
    const { deleteLevel, addLevel } = useSetupStore();
    const [showAdd, setShowAdd] = useState(false);

    const resistance = card.keyLevels.filter(l => l.type === 'resistance').sort((a, b) => b.price - a.price);
    const support = card.keyLevels.filter(l => l.type === 'support').sort((a, b) => b.price - a.price);

    const allPrices = card.keyLevels.map(l => l.price);
    if (card.currentPrice) allPrices.push(card.currentPrice);
    const maxPrice = allPrices.length ? Math.max(...allPrices) * 1.002 : 0;
    const minPrice = allPrices.length ? Math.min(...allPrices) * 0.998 : 0;
    const range = maxPrice - minPrice || 1;

    function barWidth(strength: LevelStrength) {
        return strength === 'strong' ? '90%' : strength === 'moderate' ? '65%' : '40%';
    }

    function pct(price: number) {
        return Math.max(0, Math.min(100, ((price - minPrice) / range) * 100));
    }

    const LevelRow = ({ level }: { level: KeyLevel }) => {
        const isRes = level.type === 'resistance';
        return (
            <div className="flex items-center gap-2 group">
                <span className="text-[10px] uppercase tracking-wider text-zinc-600 w-6 text-right flex-shrink-0">
                    {level.label}
                </span>
                <div className="flex-1 h-4 flex items-center">
                    <div
                        className={clsx('h-2 rounded-full transition-all duration-700', isRes ? 'bg-rose-500/60' : 'bg-emerald-500/60')}
                        style={{ width: barWidth(level.strength) }}
                    />
                </div>
                <span className="font-mono text-xs text-zinc-300 w-16 text-right">${level.price.toFixed(2)}</span>
                <button onClick={() => deleteLevel(card.id, level.id)}
                    className="opacity-0 group-hover:opacity-100 text-zinc-700 hover:text-rose-400 transition-all p-0.5">
                    <X className="h-3 w-3" />
                </button>
            </div>
        );
    };

    return (
        <div className="flex flex-col h-full">
            <div className="flex items-center justify-between mb-3">
                <span className="text-[10px] uppercase tracking-widest text-zinc-500 font-semibold">Key Levels</span>
                <button onClick={() => setShowAdd(true)}
                    className="flex items-center gap-1 text-[10px] text-blue-400 hover:text-blue-300 transition-colors">
                    <Plus className="h-3 w-3" /> Add
                </button>
            </div>

            {showAdd && <AddLevelModal cardId={card.id} onClose={() => setShowAdd(false)} />}

            {card.keyLevels.length === 0 ? (
                <div className="flex-1 flex items-center justify-center">
                    <div className="text-center">
                        <BarChart2 className="h-8 w-8 text-zinc-700 mx-auto mb-2" />
                        <p className="text-xs text-zinc-600">No levels yet</p>
                        <button onClick={() => setShowAdd(true)}
                            className="mt-2 text-xs text-blue-400 hover:text-blue-300 transition-colors">Add first level</button>
                    </div>
                </div>
            ) : (
                <div className="flex-1 space-y-1 overflow-visible">
                    {/* Resistance */}
                    {resistance.length > 0 && (
                        <div className="mb-3">
                            <p className="text-[9px] uppercase tracking-widest text-rose-500/70 mb-1.5 font-semibold">Resistance</p>
                            <div className="space-y-1.5">
                                {resistance.map(l => <LevelRow key={l.id} level={l} />)}
                            </div>
                        </div>
                    )}

                    {/* Current price line */}
                    {card.currentPrice && (
                        <div className="flex items-center gap-2 py-1">
                            <span className="text-[10px] text-sky-400 uppercase tracking-wider w-6 text-right flex-shrink-0">Now</span>
                            <div className="flex-1 h-px bg-sky-400/60 relative">
                                <div className="absolute right-0 top-1/2 -translate-y-1/2 w-1.5 h-1.5 rounded-full bg-sky-400" />
                            </div>
                            <span className="font-mono text-xs font-bold text-sky-400 w-16 text-right">${card.currentPrice.toFixed(2)}</span>
                            <div className="w-3.5" />
                        </div>
                    )}

                    {/* Support */}
                    {support.length > 0 && (
                        <div className="mt-3">
                            <p className="text-[9px] uppercase tracking-widest text-emerald-500/70 mb-1.5 font-semibold">Support</p>
                            <div className="space-y-1.5">
                                {support.map(l => <LevelRow key={l.id} level={l} />)}
                            </div>
                        </div>
                    )}
                </div>
            )}
        </div>
    );
}

