// ─── Helpers ──────────────────────────────────────────────────────────────────

const fmt = (n: number) =>
    new Intl.NumberFormat('en-US', { style: 'currency', currency: 'USD', minimumFractionDigits: 2 }).format(n);

const fmtP = (n: number) =>
    `${n >= 0 ? '+' : ''}${n.toFixed(2)}`;

function calcRR(entry: number, stop: number, target: number): number {
    const risk = Math.abs(entry - stop);
    const reward = Math.abs(target - entry);
    return risk > 0 ? parseFloat((reward / risk).toFixed(2)) : 0;
}

function formatDate(iso: string): string {
    const d = new Date(iso + 'T00:00:00');
    const today = new Date();
    const yesterday = new Date();
    yesterday.setDate(yesterday.getDate() - 1);
    if (iso === today.toISOString().slice(0, 10)) return 'Today';
    if (iso === yesterday.toISOString().slice(0, 10)) return 'Yesterday';
    return d.toLocaleDateString('en-US', { weekday: 'short', month: 'short', day: 'numeric' });
}

function todayISO() {
    return new Date().toISOString().slice(0, 10);
}

